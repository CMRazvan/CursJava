package Sesiunea4;

public class S4Ex3 {
//    Scrieți un program care să afișeze numerele de la 1 la 10.
//    Idee: folosiți ”for”.
//    Pentru ajutor aveți exemple la sfârșitul documentului.
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
    }
}
