package Sesiunea3;

public class S3Ex3 {
    public static void main(String[] args) {
        System.out.println("Scrieți un program care să afișeze numerele de la 1 la 10.\n" +
                "Idee: folosiți ”for”.\n");
        System.out.println("Avem un program care numara de la 1 la 10");
        for (int i = 1; i <= 10 ; i++){
            System.out.println(i);
        }
    }
}
